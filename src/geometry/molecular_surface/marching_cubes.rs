#[cfg(test)]
mod tests {
    use super::*;

    /// Single-cube case 1 (corner 0 only is below isolevel) emits one
    /// triangle whose vertices lie on the three edges incident to corner 0.
    #[test]
    fn case_one_emits_one_triangle() {
        // Synthetic 2×2×2 grid (one cube), with corner 0 below isolevel.
        let positions = [[0.0_f64; 3]];
        let radii = [1.0_f64];
        let grid = Grid::fit(&positions, &radii, 1.0, 0.5, 0.0);
        // Walk a 2×2×2 cube manually so the test is independent of fit().
        let g = Grid {
            origin: [0.0, 0.0, 0.0],
            spacing: 1.0,
            nx: 2,
            ny: 2,
            nz: 2,
        };
        let mut densities = vec![2.0_f64; 8];
        densities[0] = 0.0; // only corner 0 below isolevel = 1
        let (verts, faces) = polygonize(&g, &densities, 1.0);
        assert_eq!(faces.len(), 1, "case 1 should produce exactly one triangle");
        assert_eq!(
            verts.len(),
            3,
            "three vertices on the three intersected edges"
        );
        let _ = grid;
    }

    /// Empty case (all corners above) and saturated case (all corners below)
    /// both emit no geometry.
    #[test]
    fn empty_and_saturated_cases_emit_nothing() {
        let g = Grid {
            origin: [0.0, 0.0, 0.0],
            spacing: 1.0,
            nx: 2,
            ny: 2,
            nz: 2,
        };
        let above = vec![2.0_f64; 8];
        let (v, f) = polygonize(&g, &above, 1.0);
        assert!(v.is_empty() && f.is_empty());

        let below = vec![0.0_f64; 8];
        let (v, f) = polygonize(&g, &below, 1.0);
        assert!(v.is_empty() && f.is_empty());
    }

    /// Two adjacent cubes sharing a face must dedup vertices on the shared
    /// edges. Build a 3×2×2 sample grid, mark corners on the left half below
    /// isolevel, expect exactly one quad (split into two triangles) on the
    /// shared YZ plane and no duplicates.
    #[test]
    fn shared_edge_vertices_are_deduped() {
        let g = Grid {
            origin: [0.0, 0.0, 0.0],
            spacing: 1.0,
            nx: 3,
            ny: 2,
            nz: 2,
        };
        let mut densities = vec![2.0_f64; g.n_samples()];
        // Left layer (i=0) all below isolevel; right two layers above.
        for k in 0..2 {
            for j in 0..2 {
                densities[g.flat_idx(0, j, k)] = 0.0;
            }
        }
        let (verts, faces) = polygonize(&g, &densities, 1.0);
        // The separating face is at i = 0.5, a unit square ⇒ 2 triangles, 4
        // unique vertices (dedup means none of them is repeated).
        assert_eq!(faces.len(), 2, "got {} faces", faces.len());
        assert_eq!(verts.len(), 4, "got {} vertices", verts.len());
    }
}
